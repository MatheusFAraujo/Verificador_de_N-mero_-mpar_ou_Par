# Como verificar se o número é Par ou Ímpar?

numero = float(input('Dígite um número: '))
if numero % 2 == 0:
    print('Esse número é Par!')
else:
    print('Esse número é Impar!')

# Pegando um número qualquer e vendo se o resto da divisão é igual a 0,
# caso seja, então ele é um número Par, caso contrário, ele é um número Ímpar.